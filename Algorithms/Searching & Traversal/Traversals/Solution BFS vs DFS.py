# 1. If you know the solution is not far from root of the tree?
# BFS.

# 2. If the tree is very deep and solutions are very rare?
# BFS because DFS will take a very long time to complete with this type of tree.

# 3. If the tree is very wide?
# DFS because BFS will need too much memory

# 4. If the solutions are frequent but located deep in the tree?
# DFS

# 5. Determining whether a path exists between two nodes?
# DFS

# 6. Finding the shortest path?
# BFS